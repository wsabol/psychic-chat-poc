export function selectTarotCards(numCards) {
    const shuffled = [...tarotDeck].sort(() => 0.5 - Math.random());  // Shuffle the full deck
    const selected = shuffled.slice(0, numCards);
    const invertedSelected = selected.map(card => ({ ...card, inverted: Math.random() < 0.5 }));  // Invert 50% of selected cards
    return invertedSelected;
}